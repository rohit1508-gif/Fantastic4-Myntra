package com.example.myntrahackathon.ModalClasses;

public class ExchangeCard {
    String title;
    String status;

    public ExchangeCard(String title, String status) {
        this.title = title;
        this.status = status;
    }

    public String getTitle() {
        return title;
    }

    public String getStatus() {
        return status;
    }
}
